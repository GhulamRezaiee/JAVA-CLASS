package com.Syntax.Class08;

import java.util.Scanner;

public class ForLoop {
	public static void main (String [] args) {
		for(int i = 1; i<21; i++) {
			System.out.print(i+" ");
		}
		
		System.out.println();
		System.out.println("------------------------------------");
		
		
		for(int j = 20; j<50; j++) {
			if (j % 2 != 0) {
				System.out.print(j+" ");
			}
		}
		
		System.out.println();
		System.out.println("------------------------------------");
		
		
		
		
	}

}
