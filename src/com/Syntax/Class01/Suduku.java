package com.Syntax.Class01;
import java.util.Scanner;

public class Suduku {

	public static void main(String[] args) {
		double dis;
		char code = 'C';
		
		switch (code)
		{
		case 'A':
			dis = 0.0;
			System.out.println(dis);
		case 'B':
			dis = 0.1;
			System.out.println(dis);
		case 'C':
			dis = 0.2;
			System.out.println(dis);
		default:
			dis = 0.3;
			System.out.println(dis);
		}
		
	    
	}

}
